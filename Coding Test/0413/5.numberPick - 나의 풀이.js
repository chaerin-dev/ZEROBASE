// 다시 풀어본 풀이
function solution2(A, K) {
  // 숫자들을 내림차순으로 정렬
  A.sort((a, b) => b - a);
  // 몇 번째로 조합된 숫자인지 저장할 변수
  let cnt = 0;
  // 이중 for문으로 순열 구현 (i인덱스의 값: 10의자리, j인덱스의 값: 1의자리)
  for (let i = 0; i < A.length; i++) {
    for (let j = 0; j < A.length; j++) {
      // 두 인덱스가 같은 곳을 가리키지 않아야 함
      if (i !== j) {
        cnt++;
        // 조합된 수 중 K번째로 큰 숫자 반환
        if (cnt === K) return A[i] * 10 + A[j];
      }
    }
  }
}
